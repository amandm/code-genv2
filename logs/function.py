#!/usr/bin/env python3

def add_numbers(num1, num2, num3):
    """Adds three numbers together and returns the sum.

    Args:
        num1 (int): First number to be added.
        num2 (int): Second number to be added.
        num3 (int): Third number to be added.

    Returns:
        int: Sum of the three numbers.
    """
    return num1 + num2 + num3